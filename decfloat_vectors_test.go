// TestParseGDADecimalString sanity-checks the parser used by the
// byte-conversion tests above. Failures here mask real bugs in the
// formatter under test, so keep this guarded.
func TestParseGDADecimalString(t *testing.T) {
	cases := []struct {
		in       string
		wantStr  string // formatted via formatDecimalGDA after parse → round-trip
		wantNeg  bool
		wantExp  int32
		wantCoef string
	}{
		{"0", "0", false, 0, "0"},
		{"-0", "-0", true, 0, "0"},
		{"12345", "12345", false, 0, "12345"},
		{"-7.50", "-7.50", true, -2, "750"},
		{"1.2E+3", "1.2E+3", false, 2, "12"},
		{"1E-7", "1E-7", false, -7, "1"},
		{"9999999999999999E369", "9.999999999999999E+384", false, 369, "9999999999999999"},
		{"0.000000000000001E-383", "1E-398", false, -398, "1"},
		{"-1e-398", "-1E-398", true, -398, "1"},
		{"-0E+369", "-0E+369", true, 369, "0"},
	}
	for _, tc := range cases {
		t.Run(tc.in, func(t *testing.T) {
			coeff, exp, neg := parseGDADecimalString(tc.in)
			if coeff == nil {
				t.Fatalf("parse failed for %q", tc.in)
			}
			if neg != tc.wantNeg || exp != tc.wantExp || coeff.String() != tc.wantCoef {
				t.Errorf("parse %q: got (coeff=%s exp=%d neg=%v), want (coeff=%s exp=%d neg=%v)",
					tc.in, coeff, exp, neg, tc.wantCoef, tc.wantExp, tc.wantNeg)
			}
			// Round-trip through formatDecimalGDA to confirm the cohort is recoverable.
			got := formatDecimalGDA(coeff, exp, neg, "")
			if got != tc.wantStr {
				t.Errorf("round-trip %q: formatDecimalGDA returned %q, want %q", tc.in, got, tc.wantStr)
			}
		})
	}
}
