func assertCohortEqual(t *testing.T, hexStr, want, got string) {
	t.Helper()
	wantCoeff, wantExp, wantNeg := parseGDADecimalString(want)
	gotCoeff, gotExp, gotNeg := parseGDADecimalString(got)
	if wantCoeff == nil || gotCoeff == nil {
		t.Fatalf("hex=%s: parse failed (want=%q parsed=%v, got=%q parsed=%v)",
			hexStr, want, wantCoeff != nil, got, gotCoeff != nil)
	}
	if wantNeg != gotNeg || wantExp != gotExp || wantCoeff.Cmp(gotCoeff) != 0 {
		t.Errorf("hex=%s\n  want %q  cohort=(coeff=%s exp=%d neg=%v)\n  got  %q  cohort=(coeff=%s exp=%d neg=%v)",
			hexStr, want, wantCoeff, wantExp, wantNeg, got, gotCoeff, gotExp, gotNeg)
	}
}

