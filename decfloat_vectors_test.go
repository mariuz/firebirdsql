// parseGDADecimalString parses a GDA-format decimal string into its IEEE 754
// cohort (coefficient × 10^exponent, signed). Used so the byte-conversion
// vectors can compare cohorts rather than exact string forms — Java's
// BigDecimal.equals does cohort comparison, so the upstream test data carries
// multiple equivalent string spellings of the same encoded value (e.g.
// "-1E-398", "-0.000000000000001E-383", "-1e-398" all decode from the same
// bytes).
func parseGDADecimalString(s string) (coeff *big.Int, exp int32, negative bool) {
	switch {
	case strings.HasPrefix(s, "-"):
		negative = true
		s = s[1:]
	case strings.HasPrefix(s, "+"):
		s = s[1:]
	}
	mant := s
	if e := strings.IndexAny(s, "eE"); e >= 0 {
		mant = s[:e]
		v, err := strconv.Atoi(s[e+1:])
		if err != nil {
			return nil, 0, false
		}
		exp = int32(v)
	}
	if dot := strings.Index(mant, "."); dot >= 0 {
		exp -= int32(len(mant) - dot - 1)
		mant = mant[:dot] + mant[dot+1:]
	}
	coeff = new(big.Int)
	if _, ok := coeff.SetString(mant, 10); !ok {
		return nil, 0, false
	}
	return coeff, exp, negative
}

